# Operators :-

a=3
b=4
# Arithematic Operators (+,-,*,/)
print("The value of 3+4 is",a+b)
print("The value of 3-4 is",a-b)
print("The value of 3*4 is",a*b)
print("The value of 3/4 is",a/b)

# Assignment Operators (=,+=,-=,*=,/=)
a = 23
a += 100 # += this add 100 in 23
a -= 23  # -= this substract 23 in 123
a *= 10  # *= this multiple 100 with 10
a /= 20  # /= this divide 1000 with 20
print(a)

# comparison Operators(==,>,>=,<,<=,!=)
a=(2==3)#condition
print(a)# Result

b= (4>7) #condition
print(b) #result in the form of true or false

c=(5>=2) #condition
print(c)

d=(50<80)# condition
print(d)

e=(70!=50) # condition
print(e)

# Logical Operators( True, False) based on the Boolean Algebra. (and , or , not)
